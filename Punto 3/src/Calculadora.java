public class Calculadora {
    

    
    public Calculadora() {
    }
    public void suma(int valor1 ,int valor2){
        System.out.println("el resultado de la suma es: "+(valor1+valor2));
    }
    public void resta(int valor1 ,int valor2){
        System.out.println("el resultado de valor1-valor2 es: "+(valor1-valor2));
    }
    public void multiplicacion(int valor1 ,int valor2){
        System.out.println("el resultado de la multiplicacion es: "+(valor1*valor2));
    }
    public void divicion(Float valor1 ,Float valor2){
        System.out.println("el resultado de la divicion es: "+(valor1/valor2));
    }
}
