public class ConversorMoneda {

    public void convertirDolaresAEuros(double dolares) {
        System.out.println(dolares * 0.85); 
    }

    public void convertirEurosADolares(double euros) {
        System.out.println(euros * 1.18);
    }

    public void convertirPesosADolares(double pesos) {
        System.out.println(pesos * 0.00027);
    }

    public void convertirDolaresAPesos(double dolares) {
        System.out.println(dolares * 4000);
    }

    
}
